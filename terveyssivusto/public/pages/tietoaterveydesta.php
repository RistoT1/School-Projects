                    <h3 class="subsection-title">Miten vaikuttaa verenpaineeseen</h3>
                    <p class="document-text">
                        Elintavoilla on merkittävä vaikutus verenpaineeseen. Suotuisat elintapamuutokset voivat alentaa 
                        verenpainetta 5-10 mmHg. Monille tämä voi riittää verenpaineen normalisoimiseen ilman lääkitystä, 
                        etenkin lievässä verenpaineen kohoamisessa.
                    </p>
                    
                    <h4 class="subsection-title">Verenpaineeseen vaikuttavat elintapatekijät:</h4>
                    <ul class="document-list">
                        <li><strong>Suolan käyttö:</strong> Vähennä suolan käyttöä alle 5 grammaan päivässä. Vältä erityisesti 
                        piilososuolaa, jota on runsaasti valmisruoissa, leivässä ja muissa elintarvikkeissa.</li>
                        
                        <li><strong>Painonhallinta:</strong> Jo 5-10% painonpudotus ylipainoisilla voi alentaa verenpainetta 
                        merkittävästi. Vyötärönympärys miehillä alle 100 cm ja naisilla alle 90 cm on tavoiteltavaa.</li>
                        
                        <li><strong>Liikunta:</strong> Säännöllinen kestävyysliikunta (kuten kävely, uinti, pyöräily) 
                        vähintään 150 minuuttia viikossa alentaa verenpainetta keskimäärin 5-8 mmHg.</li>
                        
                        <li><strong>Alkoholin käyttö:</strong> Alkoholin käytön vähentäminen korkeintaan 2 annokseen päivässä 
                        miehillä ja 1 annokseen naisilla voi alentaa verenpainetta.</li>
                        
                        <li><strong>Tupakointi:</strong> Tupakoinnin lopettaminen on tärkeää sydän- ja verisuoniterveyden 
                        kannalta, vaikka se ei suoraan alenna verenpainetta.</li>
                        
                        <li><strong>Rentoutuminen ja stressinhallinta:</strong> Stressillä on yhteys korkeaan verenpaineeseen. 
                        Säännöllinen rentoutuminen, riittävä uni ja stressinhallintatekniikat voivat auttaa.</li>
                    </ul>
                    
